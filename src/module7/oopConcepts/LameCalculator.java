package module7.oopConcepts;

public class LameCalculator {

    static int plus(int a, int b){
        int plus = a + b;
        return plus;
    }

    static int minus(int a, int b){
        int minus = a - b;
        return minus;
    }

    static int multiply(int a, int b){
        int multiply = a * b;
        return multiply;
    }

    static int divide(int a, int b){
        int divide = a / b;
        return divide;
    }

}
